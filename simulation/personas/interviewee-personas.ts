/** ランダムにN人取得 */
export function getRandomPersonas(n: number, seed?: number): IntervieweePersona[] {
  const shuffled = [...ALL_INTERVIEWEE_PERSONAS];
  let s = seed ?? Date.now();
  for (let i = shuffled.length - 1; i > 0; i--) {
    s = (s * 1103515245 + 12345) & 0x7fffffff;
    const j = s % (i + 1);
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled.slice(0, n);
}

