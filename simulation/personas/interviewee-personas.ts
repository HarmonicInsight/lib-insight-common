/**
 * インタビュー対象ペルソナ定義
 *
 * 10業種 × 多様な役職・部署 × 課題パターン = 40名
 * 各ペルソナは固有の Pain / Insecurity / Vision / Objection / Traction を持つ
 */

export interface IntervieweePersona {
  id: string;
  name: string;
  age: number;
  gender: 'M' | 'F';
  company: string;
  industry: string;
  department: string;
  role: string;
  yearsInRole: number;
  teamSize: number;
  /** このペルソナが語る典型的なPain（課題・困りごと） */
  painPoints: string[];
  /** 不安・心配事 */
  insecurities: string[];
  /** 要望・理想像 */
  visions: string[];
  /** 抵抗・反対意見 */
  objections: string[];
  /** うまくいっていること・強み */
  tractions: string[];
  /** 使用ツール */
  tools: string[];
  /** 業務プロセスキーワード */
  processes: string[];
  /** 発言スタイル（温度感に影響） */
  speakingStyle: 'formal' | 'casual' | 'emotional' | 'reserved';
  /** 話す量 */
  verbosity: 'terse' | 'normal' | 'verbose';
}

